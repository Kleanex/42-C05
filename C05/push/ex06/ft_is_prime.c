int	ft_is_prime(int nb)
{
	int	i;

	if (nb <= 1)
		return (0);
	i = 2;
	while (i < nb && nb <= 2147483647)
	{
		if (nb != 1 && nb % i == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
