int	ft_sqrt(int nb)
{
	int	i;
	int	tmp;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i < 46341)
	{	
		tmp = i * i;
		if (tmp == nb)
			return (i);
		i++;
	}
	return (0);
}
